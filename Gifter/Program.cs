
using Gifter.Repositories;
using Microsoft.Extensions.Logging;

namespace Gifter
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();
            builder.Services.AddTransient<IPostRepository, PostRepository>();
            builder.Services.AddTransient<IUserProfileRepository, UserProfileRepository>();


            // Add CORS services-----------ADDED THIS FOR CORS
            //CORS is a browser security feature that prevents JavaScript from talking to APIs without the web server's consent.
            builder.Services.AddCors(options =>
            {
                options.AddPolicy("AllowAll",
                    policy =>
                    {
                        policy.AllowAnyOrigin()
                              .AllowAnyMethod()
                              .AllowAnyHeader();
                    });
            });

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            // Use CORS policy-----------ADDED THIS FOR CORS, MUST BE .UseAuthorization() and .MapControllers()
            app.UseCors("AllowAll");

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
